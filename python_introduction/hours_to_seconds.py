#hours_to_seconds.py

# Define the variable for hours
hours = 2  # Represents the number of hours to convert

# Calculate the number of seconds in the given hours
# There are 60 minutes in an hour and 60 seconds in a minute, so 60 * 60 = 3600 seconds in an hour.
seconds = hours * 3600

# Print the result in the specified format
print(f"{hours} hour(s) is {seconds} seconds")
