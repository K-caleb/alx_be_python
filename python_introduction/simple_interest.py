#simple_interest.py

# Define the variables for simple interest calculation
principal = 1000  # Represents $1000
rate = 0.05       # Represents 5% annual interest rate (0.05 as a decimal)
time = 3          # Represents 3 years

# Calculate the simple interest using the formula: interest = principal * rate * time
interest = principal * rate * time

# Print the calculated interest in the specified format
print(f"The simple interest is: {interest}")
